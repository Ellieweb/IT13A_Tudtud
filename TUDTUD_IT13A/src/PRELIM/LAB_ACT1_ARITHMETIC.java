public class LAB_ACT1_ARITHMETIC {
    public static void main(String[] args){
        int f = 10;
        int g = 4;
        int h = 6;
        int i = 3;
        
        int fff = f * g + h;
        int ggg = (f - g) % h;
        int hhh = (f + g + h) / i;
        int iii = f * h - (g * g);
        
        System.out.println("10 * 4 + 6 = " + fff);
        System.out.println("10 - 4 % 6 = " + ggg);
        System.out.println("10 + 4 + 6 / 3 = " + hhh);
        System.out.println("10 * 6 - 4 * 4 = " + iii);
        
                
                
    }
}
